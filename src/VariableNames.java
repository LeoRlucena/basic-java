public class VariableNames {

    public static void main(String[] args) {

        int abc;
        // int 0abc; ERRO
        int abc42;
        int $var;
        int _var;
        int va$r;
        int va__r;
        // int xp#f; ERRO

        int house;  // Follows convention
        int House;
        int HOUSE;  // Constants convention

        int numberOfYears;  // Camel-case
        int number_of_years;  // Uncommon
    }
}
